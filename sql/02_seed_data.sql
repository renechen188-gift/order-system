-- 6. 門店 × 廠商配送範圍（預設全開，你再到後台關閉不配送的）
-- ============================================
INSERT INTO inventory.store_suppliers (store_id, supplier_id, is_active)
SELECT s.id, sup.id, true
FROM inventory.stores s
CROSS JOIN inventory.suppliers sup
WHERE s.type = 'store';  -- 只對門店設定，不含央廚本身

-- ============================================
-- 7. 更新 admin 的 store_id 為 NULL（管理者不綁定門店）
-- ============================================
UPDATE inventory.users SET store_id = NULL WHERE role = 'admin';

-- ============================================
-- 驗證
-- ============================================
SELECT '門店數' AS info, COUNT(*)::text AS val FROM inventory.stores
UNION ALL
SELECT '廠商數', COUNT(*)::text FROM inventory.suppliers
UNION ALL
SELECT '品項數', COUNT(*)::text FROM inventory.items
UNION ALL
SELECT '品項廠商對應', COUNT(*)::text FROM inventory.item_suppliers
UNION ALL
SELECT '門店廠商配送', COUNT(*)::text FROM inventory.store_suppliers
UNION ALL
SELECT '使用者', COUNT(*)::text FROM inventory.users;